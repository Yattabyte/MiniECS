#include "ecsHandle.hpp"

///////////////////////////////////////////////////////////////////////////
/// Use our shared namespace mini
using namespace mini;

///////////////////////////////////////////////////////////////////////////
/// operator bool
///////////////////////////////////////////////////////////////////////////

ecsHandle::operator bool() const noexcept {
    static const ecsHandle empty;
    return !bool((*this) == empty);
}

///////////////////////////////////////////////////////////////////////////
/// isValid
///////////////////////////////////////////////////////////////////////////

bool ecsHandle::isValid() const noexcept {
    static const ecsHandle empty;
    return !bool((*this) == empty);
}